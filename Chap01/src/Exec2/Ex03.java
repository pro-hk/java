package Exec2;

import java.util.Scanner;

public class Ex03 {
	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		System.out.println("금액을 입력하시오");
		int price = scanner.nextInt();
		int 오만원 = price / 50000;
		int 만원 = (price % 50000) / 10000;
		int 천원 = (price % 10000) / 1000;
		int 백원 = (price % 1000) / 100;
		int 오십원 = (price % 100) / 50;
		int 십원 = (price % 50) / 10;
		int 일원 = price % 10;
		System.out.println("오만원권 " + 오만원 + "매");
		System.out.println("만원권 " + 만원 + "매");
		System.out.println("천원권 " + 천원 + "매");
		System.out.println("백원 " + 백원 + "개");
		System.out.println("오십원 " + 오십원 + "개");
		System.out.println("십원 " + 십원 + "개");
		System.out.println("일원 " + 일원 + "개");
		scanner.close();
	}
}